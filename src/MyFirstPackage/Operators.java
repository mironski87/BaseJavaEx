package MyFirstPackage;

public class Operators {

    public static void main(String[] args) {
        int numberOne = 100;
        int numberTwo = 30;

        System.out.println(numberOne+numberTwo);
        System.out.println(numberOne-numberTwo);
        System.out.println(numberOne*numberTwo);
        System.out.println(numberOne/numberTwo);
        System.out.println(numberOne%numberTwo);
        System.out.println(++numberOne);
        System.out.println(--numberTwo);

    }
}
